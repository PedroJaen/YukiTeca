    private void jLabel_aceptarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel_aceptarMouseClicked
        //obtenemos los datos a insertar
        String comentarios=jTextArea_comentarios.getText();
        int idUsuario=getId(jComboBox_usuario,rsUsuario);
        int idItem=getId(jComboBox_item,rsItem);
        String fPrestamo=getFecha(jSpinner_fPrestamo);
        String fEntrega=getFecha(jSpinner_fEntrega);
        //insertamos
        try {
            PreparedStatement ps=null;
            if(editar){
                ps=conn.prepareStatement("UPDATE prestamo SET id_item=?,id_uruario=?,fechaPrestamo=?,"
                        + "fechaDevolucion=?,comentarios=? WHERE id="+id);
            }
            else{
                ps=conn.prepareStatement("INSERT INTO prestamo(id_item,id_uruario,fechaPrestamo,fechaDevolucion,"
                        + "comentarios) VALUES(?,?,?,?,?)");
            }
            ps.setInt(1,idItem);
            ps.setInt(2,idUsuario);
            ps.setString(3,fPrestamo);
            ps.setString(4,fEntrega);
            ps.setString(5,comentarios);
            ps.executeUpdate();
        } catch (SQLException ex) {
            Logger.getLogger(Prestamo.class.getName()).log(Level.SEVERE, null, ex);
        }        
        dispose();
    }//GEN-LAST:event_jLabel_aceptarMouseClicked

