    //si editamos, rellenamos los campos
    private void rellenaCampos() {
        if(editar){
            id=p.getId();
            try {
                Statement st = conn.createStatement();
                String query="SELECT id_item,id_uruario,fechaPrestamo,fechaDevolucion,comentarios "
                + "FROM prestamo "
                + "WHERE id="+id;
                ResultSet rs=st.executeQuery(query);
                rs.next();
                
                int idItem=rs.getInt(1);
                setElement(jComboBox_item,idItem,"item");
                int idUsuario=rs.getInt(2);
                setElement(jComboBox_usuario,idUsuario,"usuario");
                String fechaPrestamo=rs.getString(3);
                setFecha(jSpinner_fPrestamo,fechaPrestamo);
                String fechaDevolucion=rs.getString(4);
                setFecha(jSpinner_fEntrega,fechaDevolucion);
                jTextArea_comentarios.setText(rs.getString(5));
                
            } catch (SQLException ex) {
                Logger.getLogger(Prestamo.class.getName()).log(Level.SEVERE, null, ex);
            }
        }  
    }
    
    //para rellenar el combobox de registros
    private void rellenaRegistros(){
        try {
            //consulta
            Statement st=conn.createStatement();
            rsItem=st.executeQuery("SELECT id,nombre,descripcion FROM item");
            while(rsItem.next()){
                Object[] fila = new Object[3];
                for (int i = 0; i < 3; i++) {
                    fila[i]=rsItem.getObject(i+1);
                }
                jComboBox_item.addItem(fila[1]+" "+fila[2]);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Prestamo.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    //para rellenar el combobox de usuarios
    private void rellenaUsuarios(){
        try {
            //consulta
            Statement st=conn.createStatement();
            rsUsuario=st.executeQuery("SELECT id,nombre,apellidos FROM usuario");
            while(rsUsuario.next()){
                Object[] fila = new Object[3];
                for (int i = 0; i < 3; i++) {
                    fila[i]=rsUsuario.getObject(i+1);
                }
                jComboBox_usuario.addItem(fila[1]+" "+fila[2]);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Prestamo.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    //obtenemos el id del elemento elegido en el combobox
    private int getId(JComboBox combo,ResultSet rs){
        int id=-1;
        String seleccion=combo.getSelectedItem().toString();
        //buscamos los nombres de producto en el resultser
        try {
            rs.beforeFirst();
            while(rs.next()){
                String currentElement=rs.getString(2)+" "+rs.getString(3);
                //si coinciden, ponemos la marca
                if(seleccion.equals(currentElement)){
                    id=rs.getInt(1);
                    break;
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(Prestamo.class.getName()).log(Level.SEVERE, null, ex);
        }
        return id;
    }
    
    //a partir del id, rellenamos el campo correspondiente para que no aparezca el numero
    private void setElement(JComboBox combo,int id,String campo){
        try {
            //consulta
            Statement st=st=conn.createStatement();
            ResultSet rs;
            if(campo.equals("usuario")){
                rs=st.executeQuery("SELECT nombre,apellidos FROM usuario WHERE id="+id);
            }
            else{
                rs=st.executeQuery("SELECT nombre,descripcion FROM item WHERE id="+id);
            }
            rs.next();
            String cadena=rs.getString(1)+" "+rs.getString(2);
            combo.setSelectedItem(cadena);
        } catch (SQLException ex) {
            Logger.getLogger(Prestamo.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    //obtenemos la fecha del spinner
    private String getFecha(JSpinner spinner){
        String fecha="";
        Date dia=(Date) spinner.getValue();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        fecha = sdf.format(dia);
        return fecha;
    }
    
    //colocamos la fecha en el spinner
    private void setFecha(JSpinner spinner,String fecha){
        String[] campos=fecha.split("-");
        GregorianCalendar gc=new GregorianCalendar(Integer.parseInt(campos[0]),Integer.parseInt(campos[1]),Integer.parseInt(campos[2]));
        spinner.setValue(gc.getTime());
    }
    
