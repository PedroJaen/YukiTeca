    private void jLabel_aceptarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel_aceptarMouseClicked
        //obtenemos los datos a insertar
        String nombre=jTextField_nombre.getText().trim();
        String apellidos=jTextField_apellidos.getText().trim();
        String ciudad=jTextField_ciudad.getText().trim();
        String cp=jTextField_cp.getText().trim();
        String direccion=jTextField_direccion.getText().trim();
        String mail=jTextField_mail.getText().trim();
        String provincia=jTextField_provincia.getText().trim();
        String telefono=jTextField_telefono.getText().trim();
        //comprobamos que sean correctos
        if(nombre.length()==0){
            jTextField_nombre.setBackground(Color.RED);
        }
        if(ciudad.length()==0){
            jTextField_ciudad.setBackground(Color.RED);
        }
        if(apellidos.length()==0){
            jTextField_apellidos.setBackground(Color.RED);
        }
        if(cp.length()==0){
            jTextField_cp.setBackground(Color.RED);
        }
        if(direccion.length()==0){
            jTextField_direccion.setBackground(Color.RED);
        }
        if(mail.length()==0){
            jTextField_mail.setBackground(Color.RED);
        }
        if(provincia.length()==0){
            jTextField_provincia.setBackground(Color.RED);
        }
        if(telefono.length()==0){
            jTextField_telefono.setBackground(Color.RED);
        }
        //insertamos
        if(nombre.length()>0 && ciudad.length()>0 && apellidos.length()>0 && cp.length()>0 && 
                direccion.length()>0 && mail.length()>0 && provincia.length()>0 && telefono.length()>0){
            try {
                PreparedStatement ps=null;
                if(editar){
                    ps=conn.prepareStatement("UPDATE usuario SET nombre=?,apellidos=?,mail=?,telefono=?,"
                            + "direccion=?,cp=?,ciudad=?,provincia=? WHERE id="+id);
                }
                else{
                    ps=conn.prepareStatement("INSERT INTO usuario(nombre,apellidos,mail,telefono,"
                            + "direccion,cp,ciudad,provincia) VALUES(?,?,?,?,?,?,?,?)");
                }
                ps.setString(1,nombre);
                ps.setString(2,apellidos);
                ps.setString(3,mail);
                ps.setString(4,telefono);
                ps.setString(5,direccion);
                ps.setString(6,cp);
                ps.setString(7,ciudad);
                ps.setString(8,provincia);
                ps.executeUpdate();
            } catch (SQLException ex) {
                Logger.getLogger(Usuario.class.getName()).log(Level.SEVERE, null, ex);
            }        
            dispose();
        }
        
    }//GEN-LAST:event_jLabel_aceptarMouseClicked

