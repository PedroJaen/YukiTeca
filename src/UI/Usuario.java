    //si editamos, rellenamos los campos
    private void rellenaCampos() {
        if(editar){
            id=p.getId();
            try {
                Statement st = conn.createStatement();
                String query="SELECT nombre,apellidos,mail,telefono,direccion,cp,ciudad,provincia "
                + "FROM usuario "
                + "WHERE id="+id;
                ResultSet rs=st.executeQuery(query);
                rs.next();
                jTextField_nombre.setText(rs.getString(1));
                jTextField_apellidos.setText(rs.getString(2));
                jTextField_mail.setText(rs.getString(3));
                jTextField_telefono.setText(rs.getString(4));
                jTextField_direccion.setText(rs.getString(5));
                jTextField_cp.setText(rs.getString(6));
                jTextField_ciudad.setText(rs.getString(7));
                jTextField_provincia.setText(rs.getString(8));
            } catch (SQLException ex) {
                Logger.getLogger(Usuario.class.getName()).log(Level.SEVERE, null, ex);
            }
        }  
    }
